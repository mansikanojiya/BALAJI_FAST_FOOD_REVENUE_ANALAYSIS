-RANK ITEM BASED ON TOTAL REVENUE
 SELECT ITEM_NAME,
 SUM(TRANSACTION_AMOUNT) AS TOTAL_REVENUE,
 RANK()OVER(ORDER BY SUM(TRANSACTION_AMOUNT)
 DESC) AS REVNUE_RANK 
 FROM [Balaji Fast Food Sales]
 GROUP BY item_name;

 --PERCENTAGE C ONTRIBUTION OF EACH IOTEM TO TOTAL REVENUE
 SELECT ITEM_NAME,
 SUM(TRANSACTION_AMOUNT) AS ITEM_REVENUE,
 ROUND(
   (SUM(TRANSACTION_AMOUNT)*100)/
   SUM(SUM(TRANSACTION_AMOUNT))OVER(),
 2
 )AS REVENUE_PERCENTAGE
 FROM [Balaji Fast Food Sales]
 GROUP BY ITEM_NAME;

--COMPARE CASH VS ONLINE SALES
SELECT
    SUM(CASE WHEN transaction_type = 'Cash'
             THEN transaction_amount ELSE 0 END) AS cash_revenue,
    SUM(CASE WHEN transaction_type = 'Online'
             THEN transaction_amount ELSE 0 END) AS online_revenue,
    SUM(CASE WHEN transaction_type = 'Cash'
             THEN transaction_amount ELSE 0 END)
    -
    SUM(CASE WHEN transaction_type = 'Online'
             THEN transaction_amount ELSE 0 END) AS revenue_difference
FROM [Balaji Fast Food Sales];

