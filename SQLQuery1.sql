 --FIND THE DAY WITH HIGHEST TOTAL SALES
 SELECT TOP 1 DATE,SUM(TRANSACTION_AMOUNT) AS HIGHEST_TOTAL_SALES FROM [Balaji Fast Food Sales] GROUP BY DATE
